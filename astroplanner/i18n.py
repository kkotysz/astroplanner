def _looks_like_source_text(text: object) -> bool:
    raw = str(text or "")
    if not raw:
        return False
    if raw in _SOURCE_TRANSLATIONS:
        return True
    return any(pattern.match(raw) for pattern, _translations in _PATTERN_TRANSLATIONS)


def _translate_string_property(obj: object, getter: str, setter: str, source_key: str, language: str) -> None:
    if not hasattr(obj, getter) or not hasattr(obj, setter):
        return
    try:
        current_value = getattr(obj, getter)()
    except Exception:
        return
    if not isinstance(current_value, str):
        return
    source_value = ""
    if hasattr(obj, "property"):
        try:
            stored = obj.property(source_key)
            if isinstance(stored, str):
                source_value = stored
        except Exception:
            source_value = ""
    if not source_value:
        source_value = current_value
    else:
        expected_current = translate_text(source_value, language)
        if current_value and current_value != expected_current and _looks_like_source_text(current_value):
            source_value = current_value
    if hasattr(obj, "setProperty"):
        obj.setProperty(source_key, source_value)
    translated = translate_text(source_value, language)
    if current_value != translated:
        try:
            getattr(obj, setter)(translated)
        except Exception:
            return


def localize_widget_tree(root: object, language: str | None = None) -> None:
    lang = resolve_language_choice(language or _CURRENT_LANGUAGE)
    try:
        from PySide6.QtGui import QAction
        from PySide6.QtWidgets import (
            QAbstractButton,
            QComboBox,
            QDialogButtonBox,
            QGroupBox,
            QLineEdit,
            QMenu,
            QTabWidget,
            QWidget,
        )
    except Exception:
        return

    if not isinstance(root, QWidget):
        return

    def _apply_to_widget(widget: QWidget) -> None:
        _translate_string_property(widget, "windowTitle", "setWindowTitle", "_ap_i18n_window_title_source", lang)
        _translate_string_property(widget, "toolTip", "setToolTip", "_ap_i18n_tooltip_source", lang)

        if isinstance(widget, QAbstractButton):
            _translate_string_property(widget, "text", "setText", "_ap_i18n_text_source", lang)

        if isinstance(widget, (QMenu, QGroupBox)):
            _translate_string_property(widget, "title", "setTitle", "_ap_i18n_title_source", lang)

        if isinstance(widget, QDialogButtonBox):
            standard_button_sources = {
                QDialogButtonBox.Ok: "OK",
                QDialogButtonBox.Cancel: "Cancel",
                QDialogButtonBox.Apply: "Apply",
                QDialogButtonBox.Close: "Close",
                QDialogButtonBox.Open: "Open",
            }
            for role, source_text in standard_button_sources.items():
                button = widget.button(role)
                if button is None:
                    continue
                if hasattr(button, "setProperty"):
                    button.setProperty("_ap_i18n_text_source", source_text)
                translated = translate_text(source_text, lang)
                if button.text() != translated:
                    button.setText(translated)

        if isinstance(widget, QLineEdit):
            _translate_string_property(
                widget,
                "placeholderText",
                "setPlaceholderText",
                "_ap_i18n_placeholder_source",
                lang,
            )
            return

        if hasattr(widget, "text") and hasattr(widget, "setText"):
            _translate_string_property(widget, "text", "setText", "_ap_i18n_text_source", lang)

        if isinstance(widget, QComboBox):
            for index in range(widget.count()):
                current_item = widget.itemText(index)
                stored_item = widget.itemData(index, _COMBO_SOURCE_ROLE)
                source_item = stored_item if isinstance(stored_item, str) and stored_item else current_item
                if current_item and current_item != translate_text(source_item, lang) and _looks_like_source_text(current_item):
                    source_item = current_item
                widget.setItemData(index, source_item, _COMBO_SOURCE_ROLE)
                translated_item = translate_text(source_item, lang)
                if current_item != translated_item:
                    widget.setItemText(index, translated_item)

        if isinstance(widget, QTabWidget):
            for index in range(widget.count()):
                page = widget.widget(index)
                current_tab = widget.tabText(index)
                source_tab = ""
                if page is not None and hasattr(page, "property"):
                    stored_tab = page.property("_ap_i18n_tab_title_source")
                    if isinstance(stored_tab, str):
                        source_tab = stored_tab
                if not source_tab:
                    source_tab = current_tab
                elif current_tab and current_tab != translate_text(source_tab, lang) and _looks_like_source_text(current_tab):
                    source_tab = current_tab
                if page is not None and hasattr(page, "setProperty"):
                    page.setProperty("_ap_i18n_tab_title_source", source_tab)
                translated_tab = translate_text(source_tab, lang)
                if current_tab != translated_tab:
                    widget.setTabText(index, translated_tab)

    _apply_to_widget(root)
    for child in root.findChildren(QWidget):
        _apply_to_widget(child)

    for action in root.findChildren(QAction):
        _translate_string_property(action, "text", "setText", "_ap_i18n_text_source", lang)
        _translate_string_property(action, "toolTip", "setToolTip", "_ap_i18n_tooltip_source", lang)
        _translate_string_property(action, "statusTip", "setStatusTip", "_ap_i18n_status_tip_source", lang)
