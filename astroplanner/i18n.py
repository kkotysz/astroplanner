def resolve_language_choice(value: object, system_locale: str | None = None) -> str:
    raw = str(value or "").strip().lower()
    if raw and raw in _SUPPORTED_LANGUAGE_CODES:
        return raw
    if raw in {"", "system", "auto"}:
        locale_name = str(system_locale or "").strip() or "en"
        match = re.match(r"^([a-z]{2})", locale_name.lower())
        if match and match.group(1) in _SUPPORTED_LANGUAGE_CODES:
            return match.group(1)
    return "en"


def current_language() -> str:
    return _CURRENT_LANGUAGE


def set_current_language(value: object, system_locale: str | None = None) -> str:
    global _CURRENT_LANGUAGE
    _CURRENT_LANGUAGE = resolve_language_choice(value, system_locale=system_locale)
    return _CURRENT_LANGUAGE


def translate_text(text: object, language: str | None = None) -> str:
    raw = str(text or "")
    if not raw:
        return raw
    lang = resolve_language_choice(language or _CURRENT_LANGUAGE)
    if lang == "en":
        return raw
    if "<" in raw and ">" in raw:
        return raw
    exact = _TRANSLATIONS_BY_LANGUAGE.get(lang, {}).get(raw)
    if exact is not None:
        return exact
    for pattern, localized_templates in _PATTERN_TRANSLATIONS:
        match = pattern.match(raw)
        if not match:
            continue
        template = localized_templates.get(lang)
        if not template:
            continue
        values = {key: translate_text(value, lang) for key, value in match.groupdict().items()}
        return template.format(**values)
    return raw


def set_translated_text(widget: object, source_text: object, language: str | None = None) -> str:
    source = str(source_text or "")
    if hasattr(widget, "setProperty"):
        widget.setProperty("_ap_i18n_text_source", source)
    translated = translate_text(source, language)
    if hasattr(widget, "setText"):
        widget.setText(translated)
    return translated


